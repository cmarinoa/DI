root = tk.Tk()
root.title("Ejercicio 10: Scrollbar")
root.geometry("400x400")

frame = tk.Frame(root)
frame.pack(fill="both", expand=True)

cuadro_texto = tk.Text(frame, wrap='none')
cuadro_texto.grid(row=0, column=0, sticky="nsew")

scrollbar_vert = tk.Scrollbar(frame, orient="vertical", command=cuadro_texto.yview)
scrollbar_vert.grid(row=0, column=1, sticky="ns")
cuadro_texto.config(yscrollcommand=scrollbar_vert.set)

scrollbar_hor = tk.Scrollbar(frame, orient="horizontal", command=cuadro_texto.xview)
scrollbar_hor.grid(row=1, column=0, sticky="ew")
cuadro_texto.config(xscrollcommand=scrollbar_hor.set)

frame.grid_rowconfigure(0, weight=1)
frame.grid_columnconfigure(0, weight=1)

insertar_texto()

root.mainloop()